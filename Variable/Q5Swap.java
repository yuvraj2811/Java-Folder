public class Q5Swap {
    public static void main(String[] args) {
        System.out.println("Before Swaping");
        int a=24;
        int b=45;
        System.out.println("a ="+a);
        System.out.println("b ="+b);
        int temp;
        temp=a;
        a = b;
        b=temp;
        System.out.println("After Swaping");
        System.out.println("a ="+a);
        System.out.println("b = "+b);
    }
}
